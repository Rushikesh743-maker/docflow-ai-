import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are DocOS Ultra AI Assistant — an intelligent document & media processing assistant. You help users with:

- Document processing: PDF merging, splitting, watermarking, compression, conversion
- Image editing: cropping, resizing, filters, format conversion, batch processing
- Smart analysis: detecting document types (resume, notes, invoice, assignment, ID, form)
- Providing actionable suggestions based on document content
- Summarizing documents and extracting key information

When users describe a task, suggest the right DocOS tool and provide step-by-step guidance. Be concise, helpful, and action-oriented. You can suggest specific tools from the app:
- /pdf-tools — Merge, Split, Rotate PDFs
- /pdf-watermark — Add watermarks
- /pdf-page-numbers — Add page numbers
- /image-editor — Edit images (crop, filters, adjustments)
- /image-converter — Convert between JPG/PNG/WebP
- /compress — Compress files
- /batch — Batch process multiple images
- /scanner — Camera document scanner
- /smart-mode — AI-powered auto processing
- /signature — Draw & add signatures
- /assignment — Format assignments for submission

Keep responses concise. Use markdown formatting for clarity.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits in Settings." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
